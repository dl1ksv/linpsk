MFSKVaricode::MFSKVaricode()
{
}
MFSKVaricode::~MFSKVaricode()
{
}
int MFSKVaricode::decode(unsigned int symbol)
{
	for (int i = 0; i < 256; i++)
		if (symbol == varicode[i])
			return i;
	return -1;
}
unsigned int MFSKVaricode::encode(unsigned char c)
{
return  varicode[c];
}

